// 批量删除控制标记
const deleteVisible = ref(false)

// 多选删除
const onDelete = async() => {
  const ids = []
  if (multipleSelection.value.length === 0) {
    ElMessage({
      type: 'warning',
      message: '请选择要删除的数据'
    })
    return
  }
  multipleSelection.value &&
        multipleSelection.value.map(item => {
          ids.push(item.ID)
        })
  const res = await deleteMyInviteCodeByIds({ ids })
  if (res.code === 0) {
    ElMessage({
      type: 'success',
      message: '删除成功'
    })
    if (tableData.value.length === ids.length && page.value > 1) {
      page.value--
    }
    deleteVisible.value = false
    getTableData()
  }
}

// 行为控制标记（弹窗内部需要增还是改）
const type = ref('')

// 更新行
const updateInviteCodeFunc = async(row) => {
  const res = await findInviteCode({ ID: row.ID })
  type.value = 'update'
  if (res.code === 0) {
    formData.value = res.data.reinviteCode
    dialogFormVisible.value = true
  }
}

// 删除行
const deleteInviteCodeFunc = async(row) => {
  const res = await deleteInviteCode({ ID: row.ID })
  if (res.code === 0) {
    ElMessage({
      type: 'success',
      message: '删除成功'
    })
    if (tableData.value.length === 1 && page.value > 1) {
      page.value--
    }
    getTableData()
  }
}

// 弹窗控制标记
const dialogFormVisible = ref(false)

// 查看详情控制标记
const detailShow = ref(false)

// 打开详情弹窗
const openDetailShow = () => {
  detailShow.value = true
}

// 打开详情
const getDetails = async(row) => {
  // 打开弹窗
  const res = await findInviteCode({ ID: row.ID })
  if (res.code === 0) {
    formData.value = res.data.reinviteCode
    openDetailShow()
  }
}

// 关闭详情弹窗
const closeDetailShow = () => {
  detailShow.value = false
  formData.value = {
    code: '',
    creatorUuid: '',
    newUuid: '',
    freeData: 0,
    freeDays: 0,
  }
}

// 打开弹窗
const openDialog = () => {
  type.value = 'create'
  dialogFormVisible.value = true
}

// 关闭弹窗
const closeDialog = () => {
  dialogFormVisible.value = false
  formData.value = {
    code: '',
    creatorUuid: '',
    newUuid: '',
    freeData: 0,
    freeDays: 0,
  }
}
// 弹窗确定
const enterDialog = async() => {
  elFormRef.value?.validate(async(valid) => {
    if (!valid) return
    let res
    switch (type.value) {
      case 'create':
        res = await createInviteCode(formData.value)
        break
      case 'update':
        res = await updateInviteCode(formData.value)
        break
      default:
        res = await createInviteCode(formData.value)
        break
    }
    if (res.code === 0) {
      ElMessage({
        type: 'success',
        message: '创建/更改成功'
      })
      closeDialog()
      getTableData()
    }
  })
}

const copyToClipboard = (textData) => {
  // 使用 clipboard.js 复制文本
  const clipboard = new Clipboard('.el-button', {
    text: () => textData,
  })

  clipboard.on('success', () => {
    console.log('复制成功')
    clipboard.destroy()
  })

  clipboard.on('error', () => {
    console.error('复制失败')
    clipboard.destroy()
  })

  // 触发复制动作
  clipboard.onClick()
}

