  code: '',
  creatorUuid: '',
  newUuid: '',
  freeData: 0,
  freeDays: 0,
})

// 验证规则
const rule = reactive({
})

const searchRule = reactive({
  createdAt: [
    { validator: (rule, value, callback) => {
      if (searchInfo.value.startCreatedAt && !searchInfo.value.endCreatedAt) {
        callback(new Error('请填写结束日期'))
      } else if (!searchInfo.value.startCreatedAt && searchInfo.value.endCreatedAt) {
        callback(new Error('请填写开始日期'))
      } else if (searchInfo.value.startCreatedAt && searchInfo.value.endCreatedAt &&
      (searchInfo.value.startCreatedAt.getTime() === searchInfo.value.endCreatedAt.getTime() || searchInfo.value.startCreatedAt.getTime() > searchInfo.value.endCreatedAt.getTime())) {
        callback(new Error('开始日期应当早于结束日期'))
      } else {
        callback()
      }
    }, trigger: 'change' }
  ],
})

const elFormRef = ref()
const elSearchFormRef = ref()

// =========== 表格控制部分 ===========
const page = ref(1)
const total = ref(0)
const pageSize = ref(10)
const tableData = ref([])
const searchInfo = ref({})

// 重置
const onReset = () => {
  searchInfo.value = {}
  getTableData()
}

// 搜索
const onSubmit = () => {
  elSearchFormRef.value?.validate(async(valid) => {
    if (!valid) return
    page.value = 1
    pageSize.value = 10
    getTableData()
  })
}

// 分页
const handleSizeChange = (val) => {
  pageSize.value = val
  getTableData()
}

// 修改页面容量
const handleCurrentChange = (val) => {
  page.value = val
  getTableData()
}

// 查询
const getTableData = async() => {
  const table = await getMyInviteCodeList({ page: page.value, pageSize: pageSize.value, ...searchInfo.value })
  if (table.code === 0) {
    const userUUIDArr = [] // 拿到用户uuid，去查数据

    for (var i = 0, len = table.data.list.length; i < len; i++) {
      userUUIDArr.push(table.data.list[i].newUuid)
      userUUIDArr.push(table.data.list[i].creatorUuid)
    }
    const userMapData = await batchGetUserInfoByUUID(userUUIDArr)
    for (var i2 = 0, len2 = table.data.list.length; i2 < len2; i2++) {
      const creatorUserInfo = userMapData.get(table.data.list[i2].creatorUuid)
      if (!creatorUserInfo) {
        table.data.list[i2].creatorNickName = '未知用户'
      } else {
        table.data.list[i2].creatorNickName = creatorUserInfo.nickName
      }

      const userInfo = userMapData.get(table.data.list[i2].newUuid)
      if (!userInfo) {
        table.data.list[i2].newNickName = '未知用户'
      } else {
        table.data.list[i2].newNickName = userInfo.nickName
      }
    }

    tableData.value = table.data.list
    total.value = table.data.total
    page.value = table.data.page
    pageSize.value = table.data.pageSize
  }
}

getTableData()

